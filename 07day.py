# There can be two types of Type Casting in Python â€“

# Implicit Type Casting : Python converts data type into another data type automatically.
# Explicit Type Casting : Python need user involvement to convert the variable data type into certain data type in order to the operation required.



a = 7
print(type(a))
 
# Python automatically converts
# b to float
b = 3.0
print(type(b))
 
# Python automatically converts
# c to float as it is a float addition
c = a + b
print(c)
print(type(c))
 
# Python automatically converts
# d to float as it is a float multiplication
d = a * b
print(d)
print(type(d))






